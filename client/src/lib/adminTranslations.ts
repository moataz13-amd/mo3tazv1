export function useAdminTranslation() {
  const adminLanguage = useUIStore((state) => state.adminLanguage) || 'ar';
  const t = (key: keyof typeof adminTranslations.en, fallback?: string): string => {
    return adminTranslations[adminLanguage][key] || fallback || key;
  };
  return { t, adminLanguage };
}
